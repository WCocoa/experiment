package com.qiantang.neighbourmother.business.request;

import java.io.Serializable;

/**
 * ClassName:订单详情订单id
 * author: Cocoa
 * date: 2016/10/8.
 */

public class PayResultReq implements Serializable{
    private String pay_id;//是 	普通 	string 		支付id
    private String pay_type ;//	是 	普通 	string 		订单支付1　追加支付2

    public PayResultReq() {
    }

    public PayResultReq(String pay_id, String pay_type) {
        this.pay_id = pay_id;
        this.pay_type = pay_type;
    }

    public String getPay_id() {
        return pay_id;
    }

    public void setPay_id(String pay_id) {
        this.pay_id = pay_id;
    }

    public String getPay_type() {
        return pay_type;
    }

    public void setPay_type(String pay_type) {
        this.pay_type = pay_type;
    }
}

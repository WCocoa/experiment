package com.qiantang.neighbourmother.model;

import java.io.Serializable;

/**
 * Created by next on 2016/3/24.
 */
public class LocInfoObj implements Serializable{

    private int province_id;
    private String province_name;
    private int city_id;
    private String city_name;
    private int district_id;
    private String district_name;
    private double latitude;
    private double longitude;
    private String NeighbourhoodName;

    public LocInfoObj() {
    }

    public LocInfoObj(int province_id, String province_name, int city_id, String city_name, int district_id, String district_name, double latitude, double longitude, String neighbourhoodName) {
        this.province_id = province_id;
        this.province_name = province_name;
        this.city_id = city_id;
        this.city_name = city_name;
        this.district_id = district_id;
        this.district_name = district_name;
        this.latitude = latitude;
        this.longitude = longitude;
        NeighbourhoodName = neighbourhoodName;
    }

    public int getProvince_id() {
        return province_id;
    }

    public void setProvince_id(int province_id) {
        this.province_id = province_id;
    }

    public String getProvince_name() {
        return province_name;
    }

    public void setProvince_name(String province_name) {
        this.province_name = province_name;
    }

    public int getCity_id() {
        return city_id;
    }

    public void setCity_id(int city_id) {
        this.city_id = city_id;
    }

    public String getCity_name() {
        return city_name;
    }

    public void setCity_name(String city_name) {
        this.city_name = city_name;
    }

    public int getDistrict_id() {
        return district_id;
    }

    public void setDistrict_id(int district_id) {
        this.district_id = district_id;
    }

    public String getDistrict_name() {
        return district_name;
    }

    public void setDistrict_name(String district_name) {
        this.district_name = district_name;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getNeighbourhoodName() {
        return NeighbourhoodName;
    }

    public void setNeighbourhoodName(String neighbourhoodName) {
        NeighbourhoodName = neighbourhoodName;
    }
}

package com.qiantang.neighbourmother.model;

import java.io.Serializable;

/**
 * ClassName:
 * author: Cocoa
 * date: 2016/8/17.
 */
public class PublicPosterItemObj implements Serializable {
    private String pic;
    private String text;

    public PublicPosterItemObj() {
    }

    public PublicPosterItemObj(String pic, String text) {
        this.pic = pic;
        this.text = text;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}

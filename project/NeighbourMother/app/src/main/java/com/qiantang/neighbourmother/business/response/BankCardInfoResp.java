package com.qiantang.neighbourmother.business.response;

/**
 * ClassName:银行卡信息
 * author: Cocoa
 * date: 2016/8/15.
 */
public class BankCardInfoResp {
    private String card_no;//银行卡号
    private String open_bank;//开户银行
    private String account_name;//账户姓名

    public BankCardInfoResp() {
    }

    public BankCardInfoResp(String card_no, String open_back, String account_name) {
        this.card_no = card_no;
        this.open_bank = open_back;
        this.account_name = account_name;
    }

    public String getCard_no() {
        return card_no;
    }

    public void setCard_no(String card_no) {
        this.card_no = card_no;
    }

    public String getOpen_bank() {
        return open_bank;
    }

    public void setOpen_bank(String open_bank) {
        this.open_bank = open_bank;
    }

    public String getAccount_name() {
        return account_name;
    }

    public void setAccount_name(String account_name) {
        this.account_name = account_name;
    }
}
